package com.yuejian.meet.bean;

public class IncomeListEntity {

    private String spending_type;
    private String create_time;
    private String cost_type;
    private String name;
    private String event_name;
    private String customer_id;
    private String type;
    private String op_income;

    public String getSpending_type() {
        return spending_type;
    }

    public void setSpending_type(String spending_type) {
        this.spending_type = spending_type;
    }

    public String getCreate_time() {
        return create_time;
    }

    public void setCreate_time(String create_time) {
        this.create_time = create_time;
    }

    public String getCost_type() {
        return cost_type;
    }

    public void setCost_type(String cost_type) {
        this.cost_type = cost_type;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getEvent_name() {
        return event_name;
    }

    public void setEvent_name(String event_name) {
        this.event_name = event_name;
    }

    public String getCustomer_id() {
        return customer_id;
    }

    public void setCustomer_id(String customer_id) {
        this.customer_id = customer_id;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getOp_income() {
        return op_income;
    }

    public void setOp_income(String op_income) {
        this.op_income = op_income;
    }
}
